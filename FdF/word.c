int	ft_word_count(char *str)
{
	int	cmp;
	int	nb;

	cmp = 0;
	nb = 0;
	while (str[nb] != '\0' && str[nb] != '\n')
	{
		if (str[nb] == ' ' && str[nb + 1] != '\0')
			cmp++;
		nb++;
	}
	return (cmp);
}
